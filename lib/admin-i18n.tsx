type TranslationKey = keyof typeof T.en

interface I18nCtx { lang: Lang; t: (k: TranslationKey) => string; toggle: () => void }

const Ctx = createContext<I18nCtx>({ lang: "en", t: k => T.en[k], toggle: () => {} })

export function AdminI18nProvider({ children }: { children: ReactNode }) {
  const [lang, setLang] = useState<Lang>("en")

  useEffect(() => {
    const saved = localStorage.getItem("admin_lang") as Lang | null
    if (saved === "en" || saved === "fr") setLang(saved)
  }, [])

  const toggle = () => {
    const next: Lang = lang === "en" ? "fr" : "en"
    setLang(next)
    localStorage.setItem("admin_lang", next)
  }

  const t = (k: TranslationKey) => T[lang][k] ?? T.en[k]

  return <Ctx.Provider value={{ lang, t, toggle }}>{children}</Ctx.Provider>
}

export const useI18n = () => useContext(Ctx)
